					<pre ondragstart="return false" oncopy="return false;" oncut="return false;" oncontextmenu="return false" class="note" id="html_content_263328">　　笼统地说，每个人都活在两个世界里，一个是现实世界，另一个是梦。分辨现实与梦，似乎是一件很容易的事情，但是往深处想，却并不容易。电影《盗梦空间》那个旋转的陀螺，在梦中也是可以倒下去的：因为梦可以呈现现实中的一切。

　　现实世界从来不是平面的，我们的过去会以丰富的形式和内容影响我们的现在，这使得所有的现实都有历史的纵深感。而梦比现实生活，更加具有纵深感，因为我们的感觉器官在做梦的时候相对迟钝了，所以历史事件获得了跟现实事件一样的表达机会，从这个意义上来说，梦是一个时间搅拌机，它把过去、现在还有未来混在一起，让人活在一个更加有整体感的状态中。这让人想起铃木大佐的一句话：开悟，就是一种整体感。这样说来，梦中的状态，似乎比现实状态更有价值。

　　《金刚经》里佛如是说：人生如梦又如电。这实在说出了现实和梦的最大的共同点，即都不过是大脑和身体的电生理反应而已。从这个意义上来说，释迦牟尼是人类首个最伟大的电生理学科学家。我不知道，我这样说，是褒佛还是贬佛？

　　构成一个人的梦的纵深感的内容有三个层面。一是整个人类的集体记忆。弗洛伊德赋予俄狄浦斯王的故事的意义，即对弑父娶母的禁忌，为什么基本适合所有文化中的个体，就是因为在我们记忆的最深处，都有这一共同的印记。

　　构成梦的第二层面的，是每个人类个体在今生今世的独特经历，特别是童年经历。学过精神分析的人都知道，这等于是在说这个人的人格特点。梦如其人，其实比文如其人、病如其人来得更加全面和深刻。

　　第三个层面是，这个人身处的现实冲突。这算是最浅层的了。而且，如果不考虑一个人的人格特点，这些梦里呈现的现实冲突，可以说并没有什么意义。

　　考察人类的集体记忆，一定是一件非常有趣的事情，但是，毕竟，那些东西太深了，而现实冲突又总是跟个人人格紧密相连，所以，理解梦的核心，就是从梦者的人格入手。梦是一个人的人格土壤上开出的鲜花。

　　我的职业是精神分析，分析梦是我的工作的一部分。但我对梦的兴趣跟成实宗兄相比，实在是非常小了。成兄怀着对人类内心生活的极大兴趣，多年以来，连续不断地记录和分析自己的和他人的梦，成就斐然，单就是几百万字的案例积累，就很让我这个专业的精神分析师汗颜了。

　　在成兄的文字里，梦所呈现的现实冲突、人格特点，像电影一样鲜活生动。如果说一切记忆都会寻求表达，那么，成兄的表达，对于他、我以及你，都有非同寻常的意义。

　　我知道这对我个人的意义：梦既是过去，也是未来，但最重要的，是理解我身处其中的当下。所以，梦非梦，梦是别样的现实，梦是我生命的本身。

　　祝成兄和所有喜爱成实宗作品的读者，有好吃好喝好玩，更有好梦。

